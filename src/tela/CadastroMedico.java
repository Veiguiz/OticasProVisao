        private void carregarImagem(){
        fFoto.setIcon(null);
        fFoto.setText("");
        Image img = null;
        if(getMedico().getFoto() !=null){
            img = Toolkit.getDefaultToolkit().createImage(getMedico().getFoto());
            ImageIcon icon = new ImageIcon(img);
            fFoto.setIcon(icon);
        }
    }
        
        
        
        
        
        
